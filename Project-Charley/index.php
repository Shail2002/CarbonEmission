        <ul id="right-block">
            <p>This website is to calculate the amount of carbon emitted. Calculating the total carbon emission of a building has four important stages: <strong>material production, material transportation, construction on site, and vehicle emission.</strong> Material production has the emission for production of materials, transportation has the emission of vehicle emission (fuel emission), and operational stage has lighting, water pumps, and travelling of workers. Hence, to find the life cycle assessment of materials, it is derived from the existing data concerning the production emission and emission from the transportation of vehicles. This emission stage is calculated by multiplying the materials with carbon emission coefficients. The manufacturing sector produces a carbon footprint of each material consumed by buildings. If these materials are extensively used, then the carbon footprint increases, so most essential materials with proper quantity estimation and wastage management are essential.</p>
            <p>The material transportation stage is very important, which is a major contributor to carbon emission by vehicle emission. If the transporting distance is long, the emission will be higher. Vehicle emission depends on the fuel type and efficiency of the vehicle. The vehicles used generally are the diesel engine and light-duty vehicles, medium-duty vehicles, and heavy-duty vehicles. The operational stage is where all the materials are assembled to erect the building, like bricks with mortar, concrete, steel, and other fixtures. For assembling the components of buildings need tools and devices which can be operated manually or mechanically. Nowadays, manual working methods are reduced to construct the walls and plaster in residential buildings; mostly electrically operated machines mix mortar concrete, and water is lifted from the ground using pumps.</p>
            <p>In India, extensive research on embodied carbon and carbon emissions from construction materials is underway. The Indian infrastructure sector is linked to carbon emissions, which must be greatly optimised in order to manage them. The investigation has shown a few extensively used construction materials that are important contributors to the environment's carbon footprint. The use of these materials should be monitored, and there should be a lot of adjustments in the manufacturing process. Cement, sand, coarse aggregate, fine aggregate, steel, wood, and bricks should be used sparingly and avoided in undesirable locations. <strong>These materials account for 98 percent of total carbon emissions in the construction industry</strong>. They also contribute to the pollution of the environment in manufacturing facilities.</p> 
