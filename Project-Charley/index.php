    <div class="description">
        <p>As we know there is an increase in rate of carbon in the atmosphere during construction projects,
            transportation,burning of plastics,operational stage,workers using travelling,etc. People who care about the
            environment must pay close attention. It is also critical for those who design projects that take into
            account <strong>Green Building </strong>laws. Carbon footprints and their computations have received a lot
            of attention recently as a way to reduce greenhouse gas emissions. Several carbon footprint calculators are
            accessible online to estimate individual and household carbon dioxide emissions based on housing,
            automobiles, and other consumption-related factors. On the other hand, there are only a few calculators that
            deal with building projects. Because 13- 18% of the overall embodied carbon footprint of any construction
            project and 100% of the total embodied carbon footprint of any landscape project is released or installed,
            establishing carbon calculators for construction projects will be helpful.
        <p>Increased in carbon emissions are the primary cause of climate change and global warming. The entire world
            contributes to greenhouse gas emissions, which result in an increase in global temperatures following the
            boom in the automobile and information technology industries. As the economy and G.D.P. grew, so did the
            building sector; as a result, the fabrication of construction materials rose dramatically in volume. The
            greatest approach for determining the environmental implications of construction materials across their
            whole life cycle is life cycle assessment (L.C.A.). There are numerous software applications for calculating
            CO2 emissions. The carbon emissions of embodied materials were estimated to be 748.759 kg of CO2 equivalent
            in India, whereas power consumption was estimated to be 4266.150 kg of CO2 equivalent. Assume that one want
            to reduce overall carbon emissions. In that situation, sustainable or low-carbon-emitting materials must be
            used as embodied materials. By emitting less carbon during the building's life, the power consumption over
            operating activities will be lowered.</p>
        <p>11.52 kilograms of air exchanged each day and 0.078 fractional increase in carbon dioxide the product of them
            releases 0.9 kilograms of carbon dioxide for each day per human. About 1 kilogram of carbon dioxide is
            released for every kilogram of cement made. That adds up to 3 to 4 gigatons (billions of tons) of cement,
            and of carbon dioxide emissions, produced annually today, and that amount is projected to grow.</p>
